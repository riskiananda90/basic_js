let a = 10;
let b = 5;
console.log("a+b = "+(a + b)); // Output: 15
console.log("a/b = "+(a / b)); // Output: 5
console.log("a*b = "+(a * b)); // Output: 50
console.log("a-b = "+(a - b)); // Output: 2

// Operator Penugasan
let c = 10;
c += 5; // sama dengan c = c + 5;
console.log("c += 10 : "+c); // Output: 15

// Operator Perbandingan
let d = 10;
let e = 5;
console.log("d > e = "+(d > e)); // Output: true
console.log("d < e ="+(d < e)); // Output: false

